int	ft_atoi(char *str)
{
	int	i;
	int sign;
	int result;

	i = 0;
	while (str[i] <= 13 && str[i] >= 9)
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			str[i] -= 48;
		}
		else
		i++;
	}
}

